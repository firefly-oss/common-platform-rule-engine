package com.firefly.rules.core.dsl.evaluation;

import lombok.Builder;
import lombok.Data;

import java.util.Map;

/**
 * Result of a rules evaluation containing the outcome and any generated data.
 * Can represent the result of evaluating either a single rule or multiple rules.
 */
@Data
@Builder
public class RulesEvaluationResult {

    /**
     * Whether the rule evaluation was successful
     */
    private boolean success;

    /**
     * The result of the main condition evaluation
     */
    private Boolean conditionResult;

    /**
     * Output data generated by the rule
     */
    private Map<String, Object> outputData;

    /**
     * Whether the circuit breaker was triggered
     */
    private boolean circuitBreakerTriggered;

    /**
     * Circuit breaker message if triggered
     */
    private String circuitBreakerMessage;

    /**
     * Error message if evaluation failed
     */
    private String error;

    /**
     * Execution time in milliseconds
     */
    private Long executionTimeMs;

    /**
     * Additional metadata about the evaluation
     */
    private Map<String, Object> metadata;
}
